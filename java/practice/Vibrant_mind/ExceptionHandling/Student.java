package ExceptionHandling;

public class Student {
    private int id ;
    private String name;
    private int age ;
    private String email;
    private String password ;

    Student(int id,String name , int age , String email , String password){
        this.id = id;
        this.name = name;
        this.age = age;
        this.email = email;
        this.password = password ;
    }
    
}
